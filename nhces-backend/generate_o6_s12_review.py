# ── Summary tallies ────────────────────────────────────────────────────────────

def _tally(items):
    p = sum(1 for _, _, s, _ in items if s == "PASS")
    w = sum(1 for _, _, s, _ in items if s == "WARN")
    f = sum(1 for _, _, s, _ in items if s == "FAIL")
    return len(items), p, w, f


