# ── Checklist PDF ──────────────────────────────────────────────────────────────

class ReviewPDF(DocPDF):
    def header(self):
        self.set_fill_color(*DARK_NAVY)
        self.rect(0, 0, 210, 14, "F")
        self.set_font("Helvetica", "B", 7)
        self.set_text_color(*WHITE)
        self.set_xy(5, 4)
        self.cell(PAGE_W, 6, sanitize(
            "iNHCES | TETFund NRF 2025 | ABU Zaria  |  O6-S12 Code Review Checklist"
        ))
        self.set_text_color(*DARK_GREY)
        self.ln(16)

    def footer(self):
        self.set_y(-13)
        self.set_draw_color(*GOLD)
        self.set_line_width(0.4)
        self.line(LEFT, self.get_y(), 198, self.get_y())
        self.set_font("Helvetica", "I", 7.5)
        self.set_text_color(*MID_GREY)
        self.cell(0, 8, sanitize(
            f"O6_12 Code Review Checklist  |  ABU Zaria / TETFund NRF 2025  |  Page {self.page_no()}"
        ), align="C")

    def ds_page(self, level, notes=""):
        """DATA SOURCE banner page."""
        self.add_page()
        colour = {"GREEN": (30, 130, 60), "AMBER": (180, 120, 0), "RED": (160, 30, 30)}[level]
        self.set_fill_color(*colour)
        self.set_text_color(*WHITE)
        self.rect(0, 14, 210, 22, "F")
        self.set_xy(0, 18)
        self.set_font("Helvetica", "B", 13)
        self.cell(210, 8, sanitize(f"DATA SOURCE DECLARATION — {level}"), align="C", ln=True)
        self.set_font("Helvetica", "", 9)
        labels = {
            "GREEN": "Live API / real measured instrument",
            "AMBER": "AI-authored template — verify before use",
            "RED":   "Fully synthetic / fabricated — MUST replace before publication",
        }
        self.cell(210, 7, sanitize(labels[level]), align="C", ln=True)
        self.set_text_color(*DARK_GREY)
        self.set_xy(LEFT, 44)
        if notes:
            self.set_font("Helvetica", "I", 8.5)
            self.multi_cell(PAGE_W, 5.2, sanitize(notes))
        self.ln(3)
        disclaimer = (
            "This document was generated by an AI research assistant "
            "(GitHub Copilot / Claude Sonnet 4.6). It constitutes a structural "
            "template only. All checklist items must be verified by the lead "
            "developer and sign-off authority before any production deployment. "
            "No AI-generated content should be presented as a verified security "
            "or quality audit without independent expert review. "
            "TETFund NRF 2025 | Department of Quantity Surveying, ABU Zaria."
        )
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(*MID_GREY)
        self.set_x(LEFT)
        self.multi_cell(PAGE_W, 5, sanitize(disclaimer))

    def section_band(self, title, fill=DARK_NAVY):
        self.ln(5)
        self.set_fill_color(*fill)
        self.set_text_color(*WHITE)
        self.set_font("Helvetica", "B", 10)
        self.set_x(LEFT)
        self.cell(PAGE_W, 8, sanitize(f"  {title}"), fill=True, ln=True)
        self.set_text_color(*DARK_GREY)
        self.ln(2)

    def checklist_row(self, ref, item, status, note=""):
        """
        status: PASS | WARN | FAIL | N/A
        """
        colours = {
            "PASS": PASS_GREEN,
            "WARN": WARN_AMBER,
            "FAIL": FAIL_RED,
            "N/A":  (230, 230, 230),
        }
        bg = colours.get(status, (240, 240, 240))
        self.set_fill_color(*bg)
        self.set_draw_color(*MID_GREY)
        self.set_line_width(0.2)

        # Reference cell (14 mm)
        self.set_x(LEFT)
        self.set_font("Helvetica", "B", 8)
        self.set_text_color(*DARK_NAVY)
        self.cell(14, LINE_H, sanitize(ref), border=1, fill=True)

        # Item cell
        self.set_font("Helvetica", "", 8)
        self.set_text_color(*DARK_GREY)
        item_w = 120
        self.cell(item_w, LINE_H, sanitize(item[:75]), border=1, fill=True)

        # Status cell (18 mm)
        self.set_font("Helvetica", "B", 8)
        self.set_text_color(*DARK_NAVY)
        self.cell(18, LINE_H, sanitize(status), border=1, fill=True, align="C")

        # Note cell (remaining width)
        note_w = PAGE_W - 14 - item_w - 18
        self.set_font("Helvetica", "I", 7.5)
        self.set_text_color(*MID_GREY)
        self.cell(note_w, LINE_H, sanitize(note[:35]), border=1, fill=True, ln=True)

    def checklist_header(self):
        self.set_fill_color(*DARK_NAVY)
        self.set_text_color(*WHITE)
        self.set_font("Helvetica", "B", 8)
        self.set_x(LEFT)
        cols = [
            (14, "Ref"),
            (120, "Checklist Item"),
            (18, "Status"),
            (PAGE_W - 14 - 120 - 18, "Note"),
        ]
        for w, label in cols:
            self.cell(w, LINE_H + 1, sanitize(label), border=1, fill=True, align="C")
        self.ln()

    def summary_row(self, section, total, passed, warned, failed):
        self.set_x(LEFT)
        self.set_font("Helvetica", "", 9)
        for val, w in [
            (section, 70), (str(total), 20), (str(passed), 20),
            (str(warned), 20), (str(failed), 20),
        ]:
            bg = PASS_GREEN if val == str(passed) and passed == total else (255, 255, 255)
            if val == str(failed) and failed > 0:
                bg = FAIL_RED
            self.set_fill_color(*bg)
            self.cell(w, LINE_H, sanitize(str(val)), border=1, fill=True, align="C")
        self.ln()


